import streamlit as st
import pandas as pd
import joblib

# ==========================================
# 1. PAGE CONFIGURATION
# ==========================================
st.set_page_config(
    page_title="Malaysia Flood Prediction",
    page_icon="🌧️",
    layout="wide"
)

# ==========================================
# 2. LOAD PRE-TRAINED MODEL
# ==========================================
# Using @st.cache_resource ensures the model is loaded only once, making the app faster
@st.cache_resource
def load_model():
    # Make sure 'flood_prediction_model.pkl' is in the same GitHub repository
    return joblib.load('flood_prediction_model.pkl')

try:
    model = load_model()
except Exception as e:
    st.error(f"Error loading the model. Please ensure 'flood_prediction_model.pkl' is uploaded to GitHub. Details: {e}")
    st.stop()

# ==========================================
# 3. STATE ENCODING MAPPING
# ==========================================
# NOTE: Update these integer values to match the exact encoding used in your Colab dataset
state_mapping = {
    "Johor": 1,
    "Kedah": 2,
    "Kelantan": 3,
    "Melaka": 4,
    "Negeri Sembilan": 5,
    "Pahang": 6,
    "Penang": 7,
    "Perak": 8,
    "Perlis": 9,
    "Sabah": 10,
    "Sarawak": 11,
    "Selangor": 12,
    "Terengganu": 13,
    "Kuala Lumpur": 14,
    "Labuan": 15,
    "Putrajaya": 16
}

# ==========================================
# 4. USER INTERFACE (GUI)
# ==========================================
st.title("🌧️ Malaysia Flood Prediction")
st.markdown("Decision support tool for assessing flood risk across Malaysian states and territories")
st.markdown("---")

# Create two columns layout like your screenshot
col1, col2 = st.columns(2)

with col1:
    st.subheader("1 — WEATHER PARAMETERS")

    # User Inputs (4 visible features)
    selected_state = st.selectbox("Target State / Territory", list(state_mapping.keys()))
    rainfall = st.slider("Monthly Rainfall (mm)", 0, 1000, 250)
    humidity = st.slider("Relative Humidity (%)", 0, 100, 85)
    temperature = st.number_input("Average Temperature (°C)", value=27.50, step=0.10)

    analyze_btn = st.button("▶ Run Flood Risk Analysis")

with col2:
    st.subheader("2 — PREDICTION OUTPUT")

    if analyze_btn:
        # Convert the selected state name to its corresponding numeric code
        state_code = state_mapping[selected_state]

        # ==========================================
        # 5. DATA PREPARATION (The 20 Features)
        # ==========================================
        # We combine the 4 user inputs with 16 default background values
        # to match the exact 20 features your Colab model expects.

        input_data = pd.DataFrame({
            "Year": [2026],
            "Month": [5],
            "State_Code": [state_code],                    # <-- From UI
            "District_Code": [1],
            "Temperature": [temperature],                  # <-- From UI
            "Pressure": [1010.5],
            "Dew_Point": [24.0],
            "Humidity": [humidity],                        # <-- From UI
            "Wind_Speed": [10.0],
            "Gust": [15.0],
            "Wind_Chill": [temperature],
            "UV_Index": [6.0],
            "Feel_Like_Temp": [temperature + 1.5],
            "Visibility": [10.0],
            "Solar_Radiation": [200.0],
            "Pollutant_Value": [50.0],
            "Precipitation_Rate": [0.0],
            "Precipitation_Total": [0.0],
            "Monthly_Rainfall_Current_Month": [rainfall],  # <-- From UI
            "Annual_Rainfall": [rainfall * 12]
        })

        # ==========================================
        # 6. MODEL PREDICTION
        # ==========================================
        try:
            # Run the prediction
            prediction = model.predict(input_data)

            # Display the result (Assuming 1 = Flood, 0 = No Flood)
            if prediction[0] == 1:
                st.error("🚨 **High Flood Risk Detected!**")
                st.markdown("Please take necessary precautions and alert local authorities.")
            else:
                st.success("✅ **Low Flood Risk.**")
                st.markdown("Weather conditions are currently within safe parameters.")

        except Exception as e:
            st.warning(f"Prediction Error. Please ensure the model features match. Details: {e}")

    else:
        # Default state before the button is clicked
        st.info("☁️ Set parameters on the left and click **Run Flood Risk Analysis**")